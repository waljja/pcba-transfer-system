package com.ht.vo;

public class TotalVo {
	String total;
	String smttotal;
	String cobtotal;
	String mitotal;
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getSmttotal() {
		return smttotal;
	}
	public void setSmttotal(String smttotal) {
		this.smttotal = smttotal;
	}
	public String getCobtotal() {
		return cobtotal;
	}
	public void setCobtotal(String cobtotal) {
		this.cobtotal = cobtotal;
	}
	public String getMitotal() {
		return mitotal;
	}
	public void setMitotal(String mitotal) {
		this.mitotal = mitotal;
	}
	@Override
	public String toString() {
		return "TotalVo [total=" + total + ", smttotal=" + smttotal
				+ ", cobtotal=" + cobtotal + ", mitotal=" + mitotal + "]";
	}
	
	
	 
	
	
}
